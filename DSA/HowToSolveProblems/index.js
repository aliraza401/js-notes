/* Steps to solve problems
1. Write down the key points at the top. Make sure you have all the details.
2. Make sure you double check: What are the inputs? What are the outputs?
3. What is the most important value of the problem?
4. Don't be annoying and ask too many questions.
5. Start with the naive/brute force approach.
6. Tell them why this approach is not the best (i.e. O(n^2) or higher, not readable, etc...).
7. Walk through your approach, comment things and see where you may be able to break things.
8. Before you start coding, walk through your code and write down the steps you are going to follow.
9. Modularize your code from the very beginning.
10. Start actually writing your code now. Start with the easy part.
11. Think about error checks and how you can break this code. Safeguard Code against bad input.
12. Don’t use bad/confusing names like i and j. Write code that reads well.
13. Test your code: Check for no params, 0, undefined, null, massive arrays, async code, etc…
14. Finally talk to the interviewer where you would improve the code.
15. If your interviewer is happy with the solution, the interview usually ends here. It is also
common that the interviewer asks you extension questions, such as how you would handle the
problem if the whole input is too large to fit into memory, or if the input arrives as a stream.
This is a common follow-up question at Google, where they care a lot about scale. The answer
is usually a divide-and-conquer approach — perform distributed processing of the data and only
read certain chunks of the input from disk into memory, write the output back to disk and
combine them later.
*/
